public class Pessoa{
    private String NomePessoa;
    private int Idade;
    private String Cpf;

    public void setNomePessoa(String n){
        this.Nome = n;
    }
    public String getNomePessoa(){
        return Nome;
    }
    public void setIdade(int i){
        this.Idade = i;
    }
    public int getIdade(){
        return Idade;
    }
    public void setCpf(String c){
        this.Cpf = c;
    }
    public String getCpf(){
        return Cpf;
    }
}